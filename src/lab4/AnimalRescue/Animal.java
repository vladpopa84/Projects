package lab4.AnimalRescue;

public class Animal {

    private String name;
    private String color;
    private int age;
    private int healthiness;
    private int hungryness;
    private int mood;
    private String favoriteFood;
    private String favoriteActivity;


    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }





    public void setAge(int age) {
        this.age = age;

    }

    public int getAge() {
        return age;
    }




    public void setHealthiness(int healthiness) {
        this.healthiness = healthiness;
    }

    public int getHealthiness() {
        return healthiness;
    }




    public void setHungryness(int hungryness) {
        this.hungryness = hungryness;
    }

    public int getHungryness() {
        return hungryness;
    }




    public void setMood(int mood) {
        this.mood = mood;
    }

    public int getMood() {
        return mood;
    }




    public void setFavoriteFood(String favoriteFood) {
        this.favoriteFood = favoriteFood;
    }

    public String getFavoriteFood() {
        return favoriteFood;
    }



    public void setFavoriteActivity(String favoriteActivity) {
        this.favoriteActivity = favoriteActivity;
    }

    public String getFavoriteActivity() {
        return favoriteActivity;
    }



    public String getColor() {
        return color;
    }
    public void setColor (String color) {
        this.color = color;
    }




}